void SolveAndDisplay(char[,] chars)
{
    var sudokuSolver = new SudokuSolver(chars);
    char[,] solvedBoard1 = sudokuSolver.Solve();

    for (int col = 0; col < 9; col++)
    {
        for (int row = 0; row < 9; row++)
        {
            Console.Write(solvedBoard1[col, row]);
            Console.Write("  ");
        }

        Console.WriteLine("");
    }
}













